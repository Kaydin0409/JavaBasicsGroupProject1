package ExerciseOne;

public class TaskSixSwapVariables {

	public static void main(String[] args) {
		/*
		 *  Write a program to swap 2 numbers without a temporary variable?
		 */

		
		
		
		int x=20, y=50;
		
		
		x=x+y;  //makes x=70
		y=x-y;  //makes y=20
		x=x-y; //makes x=50
		
		System.out.println("The value of x is now "+x+" and the value of y is now "+y);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
